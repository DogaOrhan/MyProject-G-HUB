
public class Deck {
	public String suits;
	public String shapes;
	
	Deck() {
		this.suits="a";
		this.suits="a";
		
	}
    //get
	public String getSuits() {
		return suits;
	}
	public String getShapes() {
		return shapes;
	}
	//set
	public void setSuits(String Suits) {
		this.suits=suits;
	}
	public void setShapes(String Shapes) {
		this.shapes=shapes;
	}
}
